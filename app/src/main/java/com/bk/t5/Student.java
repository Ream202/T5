package com.bk.t5;

public class Student {
    private String Name, Course, Matric, pics;

    public String getName() { return Name; }

    public void setName(String name) { this.Name = name; }

    public String getCourse() { return Course; }

    public void setCourse(String course) { this.Course = course; }

    public String getMatric() { return Matric; }

    public void setMatric(String matric) { this.Matric = matric; }

    public String getPics() { return pics; }

    public void setPics(String pics) { this.pics = pics; }

    public Student() { }
}